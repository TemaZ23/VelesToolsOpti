import json

d = json.load(open("output/strategy_lab/strategies_20260208_032619.json"))
print(f"Total strategies: {len(d)}")
print()
for i, s in enumerate(d[:5]):
    print(f"Strategy #{i+1}:")
    print(f"  Score: {s['score']:.2f}")
    print(f"  Median Monthly: {s['median_monthly_return']:.2f}%")
    print(f"  Mean Monthly: {s['mean_monthly_return']:.2f}%")
    print(f"  Min Monthly: {s['min_monthly_return']:.2f}%")
    print(f"  Worst DD: {s['worst_drawdown']:.1f}%")
    print(f"  Sharpe: {s['median_sharpe']:.2f}")
    print(f"  PF: {s['median_pf']:.2f}")
    print(f"  WR: {s['median_wr']:.1f}%")
    print(f"  Trades: {s['total_trades']}")
    print(f"  N Indicators: {s['n_indicators']}")
    print(f"  Leverage: {s['leverage']:.1f}x")
    print(f"  Exit Mode: {s['exit_mode']}")
    print(f"  Exit Inds: {s['n_exit_indicators']}")
    cfg = s.get("config", {})
    inds = [ind['name'] for ind in cfg.get('entry_indicators', [])]
    print(f"  Indicators: {inds}")
    print(f"  Combine: {cfg.get('combine_mode')}, thr={cfg.get('combine_threshold',0):.3f}")
    print(f"  SL={cfg.get('stop_loss_pct',0):.2f}% TP={cfg.get('take_profit_pct',0):.2f}% Trail={cfg.get('trailing_stop_pct',0):.2f}%")
    print(f"  Max Hold: {cfg.get('max_hold_bars')}, Signal Flip: {cfg.get('exit_on_signal_flip')}")
    print(f"  Time Filter: {cfg.get('time_filter_enabled')}, Hours: {cfg.get('trade_hours_start')}-{cfg.get('trade_hours_end')}")
    splits = s.get('split_returns', [])
    print(f"  Splits: {[f'{r:.1f}%' for r in splits]}")
    print()
